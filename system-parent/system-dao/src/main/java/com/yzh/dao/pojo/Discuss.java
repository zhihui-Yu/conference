package com.yzh.dao.pojo;

public class Discuss {
	private int id;
	private String uname;
	private String aname;
	private String usay;
	private String asay;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getAname() {
		return aname;
	}
	public void setAname(String aname) {
		this.aname = aname;
	}
	public String getUsay() {
		return usay;
	}
	public void setUsay(String usay) {
		this.usay = usay;
	}
	public String getAsay() {
		return asay;
	}
	public void setAsay(String asay) {
		this.asay = asay;
	}
	
}
