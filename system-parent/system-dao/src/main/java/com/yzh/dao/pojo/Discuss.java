package com.yzh.dao.pojo;

public class Discuss {
	private int id;
	private int uid;
	private int aid;
	private String usay;
	private String asay;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public String getUsay() {
		return usay;
	}
	public void setUsay(String usay) {
		this.usay = usay;
	}
	public String getAsay() {
		return asay;
	}
	public void setAsay(String asay) {
		this.asay = asay;
	}
	
}
